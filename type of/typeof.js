// tipos de dados no JavaScript.

var m = 'AlgaWorks';
console.log('M:',typeof m); // string

var n = 12;
console.log('N:', typeof n); // number

var ativo = true;
console.log('Ativo:',typeof ativo);	// boolean

var obj = {};
console.log('Objeto:',typeof obj); // object

var array = [];
console.log('Array:',typeof array);	// object

var nulo = null;
console.log('Nulo:', typeof null);	// object

var indefinido = undefined;
console.log('Indefinido:', indefinido);	// undefined

var f = function(){};
console.log('Função:', typeof f);	// function
